""" Урок 9, завдання 3
Нехай на кожну сходинку можна стати з попередньої або переступивши через одну. Визначте,
скількома способами можна піднятися на задану сходинку.
"""


def way_count(n):
    if n == 1:
        return 1
    elif n == 2:
        return 2
    else:
        return way_count(n - 1) + way_count(n - 2)


print(way_count(5))
